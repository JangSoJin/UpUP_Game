    if(s_y-0x60 <= 0x9F) draw_rectangle(s_x+0x56,s_y-0x48,0x20,0x08,1);
    if(s_y-0x60 <= 0x9F) draw_rectangle(s_x+0x24,s_y-0x60,0x20,0x08,1);
    
    if(s_y-0x78 <= 0x9F && s_y-0x78 > 0x14) draw_rectangle(s_x+0x0C,s_y-0x78,0x20,0x08,1); //처음에 안보이는부분
    
