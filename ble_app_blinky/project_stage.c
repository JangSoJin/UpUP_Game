#include "project.h"

bool dir = false; // devil 움직이는 방향 false = 오른쪽, true = 왼쪽

void stage(int s_x, int s_y){ //y간격은 0x18
    if(s_y <= 0x9F) draw_rectangle(s_x+0x12,s_y,0x45,0x08,0);
    if(s_y-0x18 <= 0x9F) draw_rectangle(s_x+0x06,s_y-0x18,0x20,0x08,0);
    if(s_y-0x18 <= 0x9F) draw_rectangle(s_x+0x51,s_y-0x18,0x20,0x08,0);
    
    if(s_y-0x30 <= 0x9F) draw_rectangle(s_x+0x09,s_y-0x30,0x20,0x08,0);
    if(s_y-0x30 <= 0x9F) draw_rectangle(s_x+0x48,s_y-0x30,0x20,0x08,0);
    if(s_y-0x48 <= 0x9F) draw_rectangle(s_x+0x15,s_y-0x48,0x20,0x08,0);
    if(s_y-0x60 <= 0x9F) draw_rectangle(s_x+0x56,s_y-0x48,0x20,0x08,0);
    if(s_y-0x60 <= 0x9F) draw_rectangle(s_x+0x24,s_y-0x60,0x20,0x08,0);
    
    if(s_y-0x78 <= 0x9F && s_y-0x78 > 0x14) draw_rectangle(s_x+0x0C,s_y-0x78,0x20,0x08,0); //처음에 안보이는부분
    
    if(s_y-0x84 <= 0x9F && s_y-0x84 > 0x14) draw_rectangle(s_x+0x0C,s_y-0x84,0x03,0x09,2); //스테이지 클리어 마크
    if(s_y-0x81 <= 0x9F && s_y-0x81 > 0x14) draw_rectangle(s_x+0x09,s_y-0x81,0x09,0x03,2);
    
    stage_clear_mark_x = s_x+0x0C;

}

